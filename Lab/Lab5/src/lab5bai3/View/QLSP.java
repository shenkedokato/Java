    private void jTableSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSPMouseClicked
        DefaultTableModel model  = (DefaultTableModel) jTableSP.getModel();

        int indexTB = jTableSP.getSelectedRow();
        if (indexTB < model.getRowCount() && indexTB >= 0 ){
            txtMaSP.setText(model.getValueAt(indexTB, 0).toString());
            txtTenSP.setText(model.getValueAt(indexTB, 1).toString());
            txtGia.setText(model.getValueAt(indexTB, 2).toString());
        }
    }//GEN-LAST:event_jTableSPMouseClicked

