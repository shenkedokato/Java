    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed
        int countResult = 0;
        // TODO add your handling code here:
        String maSP = txtMaSP.getText().trim();
        if ("".equals(txtMaSP.getText().trim()) || "".equals(txtTenSP.getText().trim()) 
                || "".equals(txtGia.getText().trim()) ) {
            JOptionPane.showMessageDialog(this, "Thiếu thông tin!");
            return;
        }
        String tenSP = txtTenSP.getText().trim();
        String gia = txtGia.getText().trim();
        
        if (txtMaSP.getText().trim().length() != 4)
        {
            JOptionPane.showMessageDialog(this, "Mã sản phẩm phải có 4 ký tự");
            return;
        }
        QuanLy ql = new QuanLy();
        countResult = ql.themSanPham(jTableSP,maSP, tenSP, gia);
        
        if(countResult == 0) {
            JOptionPane.showMessageDialog(this, "Insert không thành công!",
                    "Lỗi insert CSDL", JOptionPane.ERROR_MESSAGE);
        } else if(countResult == 1) {
              JOptionPane.showMessageDialog(this, "Thêm thành công!");
              
        }
        
        
        
    }//GEN-LAST:event_jButtonThemActionPerformed

