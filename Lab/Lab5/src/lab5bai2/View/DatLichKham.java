    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int countResult = 0;
        String maBenhNhan = jTextFieldMaBN.getText().trim();
        String yeuCau = jTextFieldYCK.getText().trim();
        String tenBacSi = (String) jComboBoxTenBS.getSelectedItem();
        DateFormat DateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String ngay = DateFormat.format(jDateChooserNK.getDate());
        if ("".equals(ngay) || "".equals(maBenhNhan) || "".equals(yeuCau)) {
            JOptionPane.showMessageDialog(this, "Thieu thong tin!");
            return;
        }
        try {
            Date ngayKham = DateFormat.parse(ngay);
            LichKhamBenh lk = new LichKhamBenh();
            countResult = lk.themLichKham(maBenhNhan, ngayKham, yeuCau, tenBacSi);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Có lỗi trong quá trình insert",
                    "Ngày sai định dạng", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DatLichKham.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(countResult == 0) {
            JOptionPane.showMessageDialog(this, "Insert không thành công!",
                    "Lỗi insert CSDL", JOptionPane.ERROR_MESSAGE);
        } else if(countResult == 1) {
              JOptionPane.showMessageDialog(this, "Thêm thành công!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

