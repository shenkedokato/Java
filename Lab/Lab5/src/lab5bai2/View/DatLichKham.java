    private void jTextFieldMaBNKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldMaBNKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            String maBenhNhan = jTextFieldMaBN.getText().trim();
            String tenBenhNhan = new BenhNhan().timBenhNhan(maBenhNhan);
            if ("".equals(tenBenhNhan)){
                JOptionPane.showMessageDialog(this, "Bệnh nhân chưa đăng ký");
                return;
            }
            jTextFieldTenBN.setText(tenBenhNhan);
            
        }       
    }//GEN-LAST:event_jTextFieldMaBNKeyPressed

